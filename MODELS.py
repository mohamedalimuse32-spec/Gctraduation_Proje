    x = GlobalAveragePooling2D(name='gap_resnet')(x)
    x = BatchNormalization(name='bn_resnet')(x)
    x = Dense(256, activation='relu', name='dense_256_resnet')(x)
    x = Dropout(0.4, name='dropout_resnet')(x)
    outputs = Dense(num_classes, activation='softmax', name='predictions_resnet')(x)

    model_resnet = Model(inputs=base_model.input, outputs=outputs)
    return model_resnet, base_model

