def visualize_predictions_balanced(generator, model, class_names, n_per_class=3):
    generator.reset()

    # Collect images per class
    class_images = {i: [] for i in range(len(class_names))}

    # Loop through batches until we have n_per_class for every class
    for images, labels in generator:
        for i in range(len(images)):
            label_idx = np.argmax(labels[i])
            if len(class_images[label_idx]) < n_per_class:
                class_images[label_idx].append((images[i], labels[i]))
        # Stop when all classes have enough images
        if all(len(v) >= n_per_class for v in class_images.values()):
            break

    # Flatten: 4 classes x 3 images = 12 images total
    all_samples = []
    for idx in range(len(class_names)):
        all_samples.extend(class_images[idx])

    fig, axes = plt.subplots(3, 4, figsize=(18, 12))
    fig.suptitle('EfficientNetB0 — Predictions on Test Images (Balanced)',
                 fontsize=16, fontweight='bold')

    for i, ax in enumerate(axes.flat):
        img, label = all_samples[i]

        # Predict
        pred = model.predict(np.expand_dims(img, axis=0), verbose=0)[0]
        true_label = class_names[np.argmax(label)]
        pred_label = class_names[np.argmax(pred)]
        confidence = np.max(pred) * 100
        correct = true_label == pred_label

        # Display
        img_display = (img - img.min()) / (img.max() - img.min())
        ax.imshow(img_display, cmap='gray')
        color = 'green' if correct else 'red'
        ax.set_title(
            f"True: {true_label}\nPred: {pred_label} ({confidence:.1f}%)",
            fontsize=9, color=color, fontweight='bold'
        )
        for spine in ax.spines.values():
            spine.set_edgecolor(color)
            spine.set_linewidth(3)
        ax.tick_params(left=False, bottom=False, labelleft=False, labelbottom=False)

    correct_patch = mpatches.Patch(color='green', label='Correct Prediction')
    wrong_patch   = mpatches.Patch(color='red',   label='Wrong Prediction')
    fig.legend(handles=[correct_patch, wrong_patch], loc='lower center',
               ncol=2, fontsize=11, bbox_to_anchor=(0.5, 0.01))

    plt.tight_layout(rect=[0, 0.04, 1, 1])
    plt.savefig('predictions_efficientnetb0.png', dpi=150, bbox_inches='tight')
