def plot_training_history(h1, h2):
    # Combine both phases
    acc     = h1.history['accuracy']     + h2.history['accuracy']
    val_acc = h1.history['val_accuracy'] + h2.history['val_accuracy']
    loss    = h1.history['loss']         + h2.history['loss']
    val_loss= h1.history['val_loss']     + h2.history['val_loss']
    epochs  = range(1, len(acc) + 1)
    phase1_end = len(h1.history['accuracy'])

    fig, axes = plt.subplots(1, 2, figsize=(16, 6))
    fig.suptitle('EfficientNetB0 — Training History', fontsize=16, fontweight='bold')

    # Accuracy
    axes[0].plot(epochs, acc,     'b-o', markersize=3, label='Train Accuracy')
    axes[0].plot(epochs, val_acc, 'r-o', markersize=3, label='Val Accuracy')
    axes[0].axvline(x=phase1_end, color='gray', linestyle='--', alpha=0.7, label='Fine-tuning starts')
    axes[0].fill_betweenx([0, 1], 1, phase1_end, alpha=0.05, color='blue', label='Phase 1')
    axes[0].fill_betweenx([0, 1], phase1_end, len(epochs), alpha=0.05, color='green', label='Phase 2')
    axes[0].set_title('Accuracy', fontsize=13)
    axes[0].set_xlabel('Epoch')
    axes[0].set_ylabel('Accuracy')
    axes[0].legend()
    axes[0].grid(True, alpha=0.3)
    axes[0].set_ylim([0, 1])

    # Loss
    axes[1].plot(epochs, loss,     'b-o', markersize=3, label='Train Loss')
    axes[1].plot(epochs, val_loss, 'r-o', markersize=3, label='Val Loss')
    axes[1].axvline(x=phase1_end, color='gray', linestyle='--', alpha=0.7, label='Fine-tuning starts')
    axes[1].fill_betweenx([0, max(loss+val_loss)], 1, phase1_end, alpha=0.05, color='blue')
    axes[1].fill_betweenx([0, max(loss+val_loss)], phase1_end, len(epochs), alpha=0.05, color='green')
    axes[1].set_title('Loss', fontsize=13)
    axes[1].set_xlabel('Epoch')
    axes[1].set_ylabel('Loss')
    axes[1].legend()
    axes[1].grid(True, alpha=0.3)

    plt.tight_layout()
    plt.savefig('training_curves_efficientnetb0.png', dpi=150, bbox_inches='tight')
    plt.show()
    print("Training curves saved.")

